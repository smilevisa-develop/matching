// 高速逆引きマップ: alias (normalized) → canonical
const ALIAS_TO_CANONICAL: Map<string, string> = (() => {
  const map = new Map<string, string>();
  for (const [canonical, aliases] of Object.entries(LABEL_ALIASES)) {
    for (const alias of aliases) {
      map.set(normalizeLabelText(alias), canonical);
    }
    map.set(normalizeLabelText(canonical), canonical);
  }
  return map;
})();

/** ラベル比較用に正規化 (空白除去 + 小文字 + 全角→半角の主要記号) */
export function normalizeLabelText(label: string): string {
  return label
    .normalize("NFKC")
    .toLowerCase()
    .replace(/[\s　:：・/／\-（）()「」"'`]/g, "");
}

/** 任意の文字列が辞書のラベルにマッチするか。マッチすれば canonical を返す。 */
export function matchLabel(input: string): string | null {
  const norm = normalizeLabelText(input);
  return ALIAS_TO_CANONICAL.get(norm) ?? null;
}

/** あるラベル候補の文字列の中から、最も近いラベル ("先頭一致" / "前方部分一致") を返す。 */
export function looseMatchLabel(input: string): string | null {
  const norm = normalizeLabelText(input);
  if (!norm) return null;
  if (ALIAS_TO_CANONICAL.has(norm)) return ALIAS_TO_CANONICAL.get(norm)!;
  for (const [alias, canonical] of ALIAS_TO_CANONICAL) {
    if (norm.startsWith(alias) || alias.startsWith(norm)) return canonical;
  }
  return null;
}
